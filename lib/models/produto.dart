class Produto {
  final String id;
  final String nome;
  final String descricao;
  final String codigo;
  final String marca;
  final int quantidade;
  final double preco;

  const Produto({
    required this.id,
    required this.nome,
    required this.descricao,
    required this.codigo,
    required this.marca,
    required this.quantidade,
    required this.preco,
  });
}
